#include "stdio.h"
#include "stdlib.h"

int	ft_any(char **tab, int (*f)(char*))
{
	int	i;

	i = 0;
	while (tab && tab[i])
	{
		if (f(tab[i++]))
			return (1);
	}
	return (0);
}
